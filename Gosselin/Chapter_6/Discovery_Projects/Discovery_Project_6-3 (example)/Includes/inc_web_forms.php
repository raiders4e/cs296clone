<h2>Web Forms and User Input</h2>
<p>User input for Web pages is done through Web forms. Web forms involve 
two components. The first is the actual Web form with fields for the user
to enter data. The second is a form processor, which reads the entered
data and responds appropriately.</p>
<p>User input for Web pages can also be done through links using the &lt;a&gt;
tag. Web browsers automatically process user input when a link is clicked, and 
send a request to the Web server.</p>

